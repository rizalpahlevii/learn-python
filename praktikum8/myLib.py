def tambah(a, b):
    return a + b


def kurang(a, b):
    return a - b


def kali(a, b):
    return a * b


def bagi(a, b):
    return a / b


def luasPersegi(sisi):
    return sisi**2


def luasSegitiga(alas, tinggi):
    return 0.5 * alas * tinggi


def luasLingkaran(jari_jari):
    phi = 3.14
    return phi * (jari_jari * jari_jari)
